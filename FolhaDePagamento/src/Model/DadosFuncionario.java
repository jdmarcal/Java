package Model;

public class DadosFuncionario {

	private String nome;
	private int hrsTrabalhadas;
	private double valorHrsTrab;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getHrsTrabalhadas() {
		return hrsTrabalhadas;
	}

	public void setHrsTrabalhadas(int hrsTrabalhadas) {
		this.hrsTrabalhadas = hrsTrabalhadas;
	}

	public double getValorHrsTrab() {
		return valorHrsTrab;
	}

	public void setValorHrsTrab(double valorHrsTrab) {
		this.valorHrsTrab = valorHrsTrab;
	}

}
